package com.course.javafundamental.percabangan;

public class IfThenElse {
    public static void main (String[] args) {
        boolean isOn = false;

        if (isOn) {
            System.out.println("Lampu nyala");
        }else {
            System.out.println("Gagal menyalakan lampu karena kondisinya false");
        }
    }
}
